// COMO SE CATALOGAN
// Se catalogan en 3, dentro de esos tres los etiquetamos.
//
// Nombre: cada patron tiene un nombre unico que en cuanto lo mencionemos dabremos cual es.(Ej: factory, flywell, astract factory).
//
// Intencion: que problema pretende resolver el patron. El patron singleton pretende resolver como crear una unica instancia de una unica clase.
// El patron builder: solventaba el problema de crear un problema complejo de forma sencilla.
//
// Tipos de patrones: Creacioneles, Estructurales, Comportamiento
//
// Patrones Creacionales: Sirven para crear instancias de objetos. Nos ayudan a encsapsular y abstraer.
//
// Patrones Prototipo: Nos permite clonar todas las propiedades de un objeto que ya existe

// Patrones Singletone: Patron creacional web MVC.

// Patrones Estructurales:
//
// Acomplan las clases independientes para que trabajemos mejor con ellas.

// Habitualmente solucionan diseno de composicion o agregacion de clases y objetos.
// Patron adapter: Une clases distintas.

// Patrones de comportamiento:
//
// Reaccionan ante algo.

// Patron observer:
// El receptor mira lo que esta haciendo el emisor y esta atento.

